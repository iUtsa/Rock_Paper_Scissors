public class Elements {
    private int rock;
    private int paper;
    private int scissors;

    public void setRock(int n){
        this.rock=n;
    }
    public int getRock(){
        return rock;
    }

    public void setPaper(int m){
        this.paper=m;
    }
    public int getPaper(){
        return paper;
    }

    public void setScissors(int o){
        this.scissors=o;
    }

    public int getScissors(){
        return scissors;
    }
}
